package module;

public class Ephemeride {
    private String dateJour;
    private String feteJour;
    private Integer jourAnnee;
    private Integer joursRestants;
    private Integer numSemaines;

    public String getDateJour() {
        return dateJour;
    }

    public void setDateJour(String dateJour) {
        this.dateJour = dateJour;
    }

    public String getFeteJour() {
        return feteJour;
    }

    public void setFeteJour(String feteJour) {
        this.feteJour = feteJour;
    }

    public Integer getJourAnnee() {
        return jourAnnee;
    }

    public void setJourAnnee(Integer jourAnnee) {
        this.jourAnnee = jourAnnee;
    }

    public Integer getJoursRestants() {
        return joursRestants;
    }

    public void setJoursRestants(Integer joursRestants) {
        this.joursRestants = joursRestants;
    }

    public Integer getNumSemaines() {
        return numSemaines;
    }

    public void setNumSemaines(Integer numSemaines) {
        this.numSemaines = numSemaines;
    }
}
